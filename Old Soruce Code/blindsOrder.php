			if(width > 2.4 || width < 0.4){
				alert("The width must be less than 2400mm and greater than 400mm");
				valid = false;
				//return false;
			}
			
			area = width*height;
			
			type = $("#item\\["+theItemNum+"\\]\\[material\\]").attr('value');
			
			switch(type)
			{	
				case "PVC":
				case "Basswood":
					price = 75.00;
					pricemin = 75.00;
					break;
				case "Cedar":
					price = 85.00;
					pricemin = 85.00;
					break;
			}
			total = (area * price >= pricemin) ? area*price : pricemin;
			
			if(surcharge == 1){
				extra = 0;
			}else{
				extra = area*surcharge;
			}
			
			//total = (total+extra);
			total = (total * quantity);
			
			//add total to form
			
			$("#item\\["+theItemNum+"\\]\\[price\\]").val(total);
			
			runningTotal+=total;
			delivery+=extra;
		});
		
		numItems = 0;
		$("#contact_form").find('select[id^=qty]').each(function()
		{
			numItems += parseInt($(this).attr('value'));
		});

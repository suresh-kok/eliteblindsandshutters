			result = "Invalid";
		}else{
			result = "$"+(parseFloat(runningTotal+delivery).toFixed(2));
		}
		$(".price").html(result);
		$("#delivery").val(delivery);
		
		if(result != "Invalid"){
			return valid;
		}else{
			return false;
		}
	}

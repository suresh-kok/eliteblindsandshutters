	function calculate()
	{
		var valid = true;
		
		runningTotal=0;
		delivery=0;
		
		validForm = validateForm();
		if (!validForm) { $(".price").html("Invalid"); valid = false; return false; }
		
		$("#contact_form").find('div[id^=item]').each(function()
		{
			
			theID = $(this).attr('id');
			theItemNumStart = theID.search("\\[") + 1;
			theItemNumEnd = theID.search("\\]") - theItemNumStart;
			theItemNum = theID.substr(theItemNumStart,theItemNumEnd);
			
			quantity = $("#item\\["+theItemNum+"\\]\\[qty\\]").attr('value');
